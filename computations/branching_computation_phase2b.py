# continue until no new elements.

def algebraic_closure_in_space(generators, Q_space, max_iter=5, tol=1e-8):
    """
    Compute the algebraic closure of generators within a vector space.

    generators: list of n x n matrices
    Q_space: orthonormal basis for the ambient space (real representation)

    Returns: orthonormal basis Q for the closure, and the basis matrices.
    """
    n_dim = generators[0].shape[0]
    n_flat = n_dim * n_dim

    # Start with generators
    current = list(generators)
    current_vecs = [np.concatenate([T.flatten().real, T.flatten().imag]) for T in current]
    Q_cur = orth(np.column_stack(current_vecs))
    d_cur = Q_cur.shape[1]

    for iteration in range(max_iter):
        # Generate products of existing basis with generators
        cur_basis = [(Q_cur[:, k][:n_flat] + 1j * Q_cur[:, k][n_flat:]).reshape(n_dim, n_dim)
                     for k in range(d_cur)]

        new_vecs = []
        for T1 in cur_basis:
            for T2 in generators:
                prod = T1 @ T2
                # Project out existing space
                pv = np.concatenate([prod.flatten().real, prod.flatten().imag])
                # Check if in ambient space
                coeffs_amb = Q_space.T @ pv
                resid_amb = np.linalg.norm(pv - Q_space @ coeffs_amb)
                if resid_amb > 0.1:
                    continue  # product left the commutant
                # Project to ambient space
                pv_proj = Q_space @ coeffs_amb
                # Check if new w.r.t. current basis
                coeffs_cur = Q_cur.T @ pv_proj
                resid = np.linalg.norm(pv_proj - Q_cur @ coeffs_cur)
                if resid > tol:
                    new_vecs.append(pv_proj)

        if not new_vecs:
            break

        # Add new vectors
        all_vecs = np.column_stack([Q_cur] + [np.column_stack(new_vecs)])
        Q_new = orth(all_vecs)
        d_new = Q_new.shape[1]

        if d_new == d_cur:
            break

        print(f"    Iteration {iteration+1}: dim {d_cur} -> {d_new} (+{d_new - d_cur})")
        Q_cur = Q_new
        d_cur = d_new

    basis_mats = [(Q_cur[:, k][:n_flat] + 1j * Q_cur[:, k][n_flat:]).reshape(n_dim, n_dim)
                  for k in range(d_cur)]
    return Q_cur, basis_mats, d_cur


