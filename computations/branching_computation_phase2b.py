def make_H_gen(label):
    """
    Build 32x32 quaternion generator.
    label: '1', 'i', 'j', 'k'
    """
    T_plus = np.zeros((16, 16), dtype=complex)

    if label == '1':
        T_plus = np.eye(16, dtype=complex)
    elif label == 'i':
        # diag(i,-i) on doublets, i on RH-up, -i on RH-down
        for idx in RH_UP: T_plus[idx, idx] = 1j
        for idx in RH_DOWN: T_plus[idx, idx] = -1j
        for (up, dn) in LH_DOUBLETS:
            T_plus[up, up] = 1j
            T_plus[dn, dn] = -1j
    elif label == 'j':
        # ((0,1),(-1,0)) on doublets, 0 on RH
        for (up, dn) in LH_DOUBLETS:
            T_plus[up, dn] = 1.0
            T_plus[dn, up] = -1.0
    elif label == 'k':
        # ((0,i),(i,0)) on doublets, 0 on RH
        for (up, dn) in LH_DOUBLETS:
            T_plus[up, dn] = 1j
            T_plus[dn, up] = 1j

    T = np.zeros((32, 32), dtype=complex)
    T[:16, :16] = T_plus
    T[16:, 16:] = G5 @ np.conj(T_plus) @ G5
    return T

