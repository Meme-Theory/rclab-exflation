print("\n" + "=" * 72)
print("[PART 7] Finding order-zero subalgebra within each factor")
print("=" * 72)

# For a factor F_i with within-factor order-zero violation, we need to find
# the maximal subalgebra S_i of F_i such that [S_i, o(S_i)] = 0.
#
# For a simple factor M_k(K), the maximal order-zero subalgebra depends on
# how o acts:
# - If o fixes the factor (o: F_i -> F_i), the o-zero subalgebra is the
#   FIXED-POINT algebra of the anti-automorphism o|_{F_i}
# - If o swaps two factors (o: F_i -> F_j), then S = F_i + F_j trivially
#   satisfies [F_i, o(F_j)] = [F_i, F_i'] where F_i' lives in a different
#   factor slot, so they commute.
#
# KEY INSIGHT: We need to understand the o-map's action on the factor ALGEBRA,
# not just the factor SUBSPACE. The o-map is an ANTI-homomorphism.

# For each factor, compute the o-restriction
print("\n  o-map restricted to each factor:")
for fi in range(n_factors):
    Q_fi = factor_bases_40[fi]
    d_fi = Q_fi.shape[1]

    # The o-map in the 40-dim basis: O_map
    # Restricted to factor fi: O_fi = Q_fi^T O_map Q_fi
    O_fi = Q_fi.T @ O_map @ Q_fi

    # How much leaks out?
    O_fi_full = O_map @ Q_fi  # d_AF x d_fi
    proj_back = Q_fi @ (Q_fi.T @ O_fi_full)
    leak = np.max(np.abs(O_fi_full - proj_back))

    # Check anti-homomorphism: o(ab) = o(b)o(a) on this factor
    # (This tests whether o is really an anti-hom on this factor)

    print(f"  Factor {fi} (rank {d_fi}): o-leak = {leak:.4f}, "
          f"o-within rank = {np.linalg.matrix_rank(O_fi, tol=1e-6)}")

    # If leak is significant, o maps this factor to another factor
    if leak > 0.1:
        # Find which factor the o-images land in
        for fj in range(n_factors):
            if fj == fi:
                continue
            Q_fj = factor_bases_40[fj]
            proj_to_fj = Q_fj @ (Q_fj.T @ O_fi_full)
            frac = np.linalg.norm(proj_to_fj) / max(np.linalg.norm(O_fi_full), 1e-15)
            if frac > 0.1:
                print(f"    o(F{fi}) -> F{fj}: fraction = {frac:.4f}")

# Now let's try the definitive approach: iterative pruning on the FULL algebra
# but using the BILINEAR structure of order-zero.
print("\n" + "-" * 60)
print("  ITERATIVE BICOMMUTANT APPROACH")
print("-" * 60)

# Start with the full 40-dim closure algebra.
# Compute: for each element a, the set of elements b such that [a, o(b)] = 0.
# Then: find the maximal subspace S = {a : [a, o(b)] = 0 for all b in S}
# by alternating projection.

# Phase 1: Compute o-commutant of the FULL algebra first
# oComm(A) = {a in A : [a, o(b)] = 0 for all b in A}
# This is a LINEAR problem: for each a, the condition is sum_i a_i F^k_{ij} = 0
# for all j, k. I.e., a is in the null space of F^k viewed as linear maps.

# Flatten F_tensor: for each j, the map a -> F(a, e_j) must be zero for all j.
# That means: the matrix M_j with M_j[k, i] = F^k_{ij} must satisfy M_j @ a = 0.
# Stack all M_j: big matrix (d_AF * d_AF) x d_AF
big_F = np.zeros((d_AF * d_AF, d_AF))
for j in range(d_AF):
    big_F[j * d_AF:(j + 1) * d_AF, :] = F_tensor[:, :, j]

oComm_A = null_space(big_F, rcond=1e-8)
d_oComm = oComm_A.shape[1]
print(f"\n  oComm(full 40-dim) = {{a : [a, o(b)]=0 for all b}} : dim = {d_oComm}")

# This is the CENTER of the opposite algebra action. It should be the same
# as Comm(o(A)) intersected with A.

# Phase 2: Compute the reverse: {b in A : [a, o(b)] = 0 for all a in A}
# I.e., o(b) commutes with all of A, meaning o(b) is in the center of A.
# Since o is an anti-automorphism, this means b is in o^{-1}(center) = o(center).
big_F_rev = np.zeros((d_AF * d_AF, d_AF))
for i in range(d_AF):
    big_F_rev[i * d_AF:(i + 1) * d_AF, :] = F_tensor[:, i, :]

oCent_A = null_space(big_F_rev, rcond=1e-8)
d_oCent = oCent_A.shape[1]
print(f"  {{b : [a, o(b)]=0 for all a}} : dim = {d_oCent}")
print(f"  (Should equal center dim = {center_dim} since o(center)=center)")

# Phase 3: ALTERNATING PROJECTION
# Start with S0 = full algebra (dim 40)
# S1 = {a in A : [a, o(b)] = 0 for all b in S0} = oComm(S0) = center-like (dim ~5-8)
# S2 = {a in A : [a, o(b)] = 0 for all b in S1} = larger (since S1 is smaller)
# Continue until S_n = S_{n+1} (fixed point)
# The fixed point is the maximal order-zero subalgebra IF it's a subalgebra.

print("\n  Alternating projection:")
S_cur = np.eye(d_AF)  # Start with full algebra
d_S_cur = d_AF

for step in range(20):
    # Compute oComm(S_cur) = {a in A : [a, o(b)] = 0 for all b in S_cur}
    # The constraint is: for all beta, k: sum_i a_i F^k_{i,j} (S_cur)_{j,beta} = 0
    # Equivalently: sum_i a_i (F^k @ S_cur)_{i, beta} = 0 for all k, beta
    # Build constraint matrix: rows are (k, beta), columns are i

    constraints = []
    for k in range(d_AF):
        # F_k = F_tensor[k, :, :]  shape (d_AF, d_AF)
        # F_k @ S_cur: (d_AF, d_S_cur) -- but we want sum_i a_i F_k[i, j] S_cur[j, beta]
        # = sum_i a_i (F_k @ S_cur)[i, beta]... wait, F_tensor[k, i, j] is the (k,i,j) entry
        # The constraint is: sum_i a_i * sum_j F[k,i,j] * S_cur[j, beta] = 0
        # = sum_i a_i * (F[k] @ S_cur)[i, beta] = 0
        F_k_S = F_tensor[k] @ S_cur[:, :d_S_cur]  # shape (d_AF, d_S_cur)
        for beta in range(d_S_cur):
            constraints.append(F_k_S[:, beta])  # row = constraint on a

    constraint_mat = np.array(constraints)  # shape (d_AF * d_S_cur, d_AF)
    oComm_S = null_space(constraint_mat, rcond=1e-8)
    d_oComm_S = oComm_S.shape[1]

    # Now compute the REVERSE: {b in A : [a, o(b)] = 0 for all a in oComm_S}
    constraints_rev = []
    for k in range(d_AF):
        # sum_j b_j * sum_i oComm_S[i, alpha] * F[k,i,j] = 0 for all alpha
        # = sum_j b_j * (oComm_S^T @ F[k])[:, j] = 0... let me be precise:
        # F[k,i,j], alpha indexes oComm_S columns
        # sum_j b_j (sum_i oComm_S[i,alpha] F[k,i,j]) = 0
        # = sum_j b_j (oComm_S[:,alpha]^T @ F[k,:,j]) = 0... no
        # Let M[alpha,j] = sum_i oComm_S[i,alpha] F[k,i,j]
        # = (oComm_S.T @ F[k])[alpha, j]
        M_alpha_j = oComm_S.T @ F_tensor[k]  # shape (d_oComm_S, d_AF)
        for alpha in range(d_oComm_S):
            constraints_rev.append(M_alpha_j[alpha, :])  # row constraining b

    constraint_mat_rev = np.array(constraints_rev)
    S_new = null_space(constraint_mat_rev, rcond=1e-8)
    d_S_new = S_new.shape[1]

    print(f"  Step {step+1}: oComm(S) dim={d_oComm_S}, "
          f"oComm^{{-1}}(oComm(S)) dim={d_S_new}")

    if d_S_new == d_S_cur:
        print(f"  Converged at dim = {d_S_new}")
        S_cur = S_new
        d_S_cur = d_S_new
        break

    S_cur = S_new
    d_S_cur = d_S_new

# The fixed point S_cur is our candidate order-zero set
print(f"\n  Fixed-point set dimension: {d_S_cur}")

# Check if it's a subalgebra
if d_S_cur > 0:
    max_closure_S = 0
    for alpha in range(d_S_cur):
        for beta in range(d_S_cur):
            ca = S_cur[:, alpha]
            cb = S_cur[:, beta]
            prod_k = np.einsum('i,j,ijk->k', ca, cb, struct_const)
            proj = S_cur.T @ prod_k
            resid = np.linalg.norm(prod_k - S_cur @ proj)
            max_closure_S = max(max_closure_S, resid)
    print(f"  Subalgebra closure: {max_closure_S:.2e} "
          f"({'PASS' if max_closure_S < 1e-6 else 'FAIL'})")

    # Verify order-zero on this set
    max_oz_S = 0
    for alpha in range(d_S_cur):
        for beta in range(d_S_cur):
            va = S_cur[:, alpha]
            vb = S_cur[:, beta]
            comm_k = np.einsum('i,kij,j->k', va, F_tensor, vb)
            max_oz_S = max(max_oz_S, np.max(np.abs(comm_k)))
    print(f"  Order-zero check: max = {max_oz_S:.2e} "
          f"({'PASS' if max_oz_S < 1e-6 else 'FAIL'})")

    if max_closure_S > 1e-6:
        print("\n  Computing algebraic closure of the fixed-point set...")
        S_mats_fp = [sum(S_cur[i, alpha] * AF_closure_basis[i]
                         for i in range(d_AF))
                     for alpha in range(d_S_cur)]
        Q_Salg, Salg_basis, d_Salg = algebraic_closure_in_space(
            S_mats_fp, Q_AF_closure, max_iter=5)
        print(f"  Algebraic closure dimension: {d_Salg}")

        # Check order-zero on the algebraic closure
        max_oz_alg = 0
        for i in range(d_Salg):
            for j in range(d_Salg):
                a = Salg_basis[i]
                b_opp = Xi @ Salg_basis[j].T @ Xi
                comm = a @ b_opp - b_opp @ a
                err = np.max(np.abs(comm))
                max_oz_alg = max(max_oz_alg, err)
        print(f"  Order-zero on closure: {max_oz_alg:.2e} "
              f"({'PASS' if max_oz_alg < 1e-6 else 'FAIL'})")

        if max_oz_alg < 1e-6:
            print(f"\n  ** CANDIDATE A_F found: dim = {d_Salg} **")
            # Analyze this algebra
            sc_cand = np.zeros((d_Salg, d_Salg, d_Salg))
            Q_cand_40 = np.zeros((d_AF, d_Salg))
            for i in range(d_Salg):
                pv = np.concatenate([Salg_basis[i].flatten().real,
                                     Salg_basis[i].flatten().imag])
                Q_cand_40[:, i] = Q_AF_closure.T @ pv

            for i in range(d_Salg):
                for j in range(d_Salg):
                    prod = Salg_basis[i] @ Salg_basis[j]
                    pv = np.concatenate([prod.flatten().real, prod.flatten().imag])
                    sc_cand[i, j, :] = Q_cand_40.T @ pv

            # Center
            comm_cand = sc_cand - sc_cand.transpose(1, 0, 2)
            rows_c = [comm_cand[:, j, k] for j in range(d_Salg) for k in range(d_Salg)]
            center_dim_cand = null_space(np.array(rows_c), rcond=1e-8).shape[1]
            print(f"  Center dimension: {center_dim_cand} (A_F target: 3)")

            # Trace form
            tf_cand = np.array([[np.trace(Salg_basis[i] @ Salg_basis[j]).real
                                 for j in range(d_Salg)] for i in range(d_Salg)])
            tf_rank_cand = np.linalg.matrix_rank(tf_cand, tol=1e-6)
            print(f"  Trace form rank: {tf_rank_cand} "
                  f"({'SEMISIMPLE' if tf_rank_cand == d_Salg else 'RADICAL'})")


