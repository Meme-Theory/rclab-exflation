# Build flat_idx(i,j) mapping
# From branching_computation.py:
#   idx 0: a = (0,0)
#   idx 1-3: c = (0,1), (0,2), (0,3)  [row 0, cols 1-3]
#   idx 4-6: b = (1,0), (2,0), (3,0)  [rows 1-3, col 0]
#   idx 7-15: D = 3x3 row-major: (1,1)=7, (1,2)=8, (1,3)=9, (2,1)=10, ...
def flat_idx(row, col):
    """Convert 4x4 internal matrix position (row, col) to flat 16-index.

    Baptista convention:
      (0,0) -> 0 (a = nu_R)
      (0,j) -> j for j=1,2,3 (c = u_R colors)
      (i,0) -> i+3 for i=1,2,3 (b = e_R, nu_L, e_L)
      (i,j) -> 7 + 3*(i-1) + (j-1) for i,j=1,2,3 (D = quarks)
    """
    if row == 0 and col == 0:
        return 0
    elif row == 0:
        return col  # 1, 2, 3
    elif col == 0:
        return row + 3  # 4, 5, 6
    else:
        return 7 + 3 * (row - 1) + (col - 1)  # 7-15

