# Define Fermi-Dirac occupation
def fermi_dirac(lam, beta, mu):
    """Fermi-Dirac distribution n_F = 1/(exp(beta*(lam - mu)) + 1)

    IMPORTANT: lam is the SIGNED eigenvalue. Do NOT use |lam|.
    For positive eigenvalue lambda_k:  call fermi_dirac(+lambda_k, beta, mu)
    For negative eigenvalue -lambda_k: call fermi_dirac(-lambda_k, beta, mu)
    """
    x = beta * (lam - mu)  # SIGNED eigenvalue, not |lam|
    # Numerically stable computation
    return np.where(x > 500, 0.0, np.where(x < -500, 1.0, 1.0 / (np.exp(x) + 1.0)))

# Define BCS occupation (Bogoliubov)
def bcs_occupation(lam, mu, Delta):
    """BCS occupation n_k = (1/2)(1 - (|lam|-mu)/E_k) where E_k = sqrt((|lam|-mu)^2 + Delta^2)"""
    xi_k = np.abs(lam) - mu
    E_k = np.sqrt(xi_k**2 + Delta**2)
    return 0.5 * (1.0 - xi_k / E_k)

