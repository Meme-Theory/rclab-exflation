            # Symmetric (metric Lie derivative) for comparison
            S_a, Lg_sym = kosmann_operator_symmetric(Gamma, gammas, a)

            K_norm = np.sqrt(np.sum(np.abs(K_a)**2))
            S_norm = np.sqrt(np.sum(np.abs(S_a)**2))
            A_norm = np.sqrt(np.sum(A_antisym**2))

            K_norms[a] = K_norm
            K_antisym_norms[a] = A_norm
            S_sym_norms[a] = S_norm

            # Check Hermiticity
