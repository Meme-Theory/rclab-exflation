def kosmann_operator_antisymmetric(Gamma, gammas, a):
    """
    Compute the Kosmann spinorial correction K_a using the ANTISYMMETRIC
    covariant derivative formula (Baptista Paper 17, eq 4.1).

    K_a = -(1/8) sum_{r,s} [Gamma^s_{ra} - Gamma^r_{sa}] gamma_r gamma_s

    where Gamma^b_{ca} are connection coefficients: nabla_{e_c} e_a = Gamma^b_{ca} e_b.

    The antisymmetric tensor A^a_{rs} = Gamma^s_{ra} - Gamma^r_{sa} vanishes
    when e_a is Killing (because nabla_{(r} X_{s)} = 0 => nabla is antisymmetric
    in (r,s) after lowering, which means Gamma^s_{ra} = -Gamma^r_{sa} already
    from metric compatibility... wait, that would make A = 2*Gamma, not zero).

    Let me re-derive carefully:
    - Metric compatibility: Gamma^c_{ab} = -Gamma^b_{ac} (antisymmetric in upper + 2nd lower)
    - Torsion-free: Gamma^c_{ab} - Gamma^c_{ba} = ft^c_{ab} (structure constants)

    For a Killing field e_a:
        (L_{e_a} g)(e_r, e_s) = Gamma^s_{ra} + Gamma^r_{sa} = 0

    The antisymmetric part:
        A^a_{rs} = Gamma^s_{ra} - Gamma^r_{sa}

    Using metric compatibility Gamma^s_{ra} = -Gamma^r_{as}... wait, that's
    Gamma^{upper}_{lower lower}. Let me be very explicit:

    Gamma^b_{ca} means: (nabla_{e_c} e_a) = Gamma^b_{ca} e_b
    Index order: Gamma[upper_b, lower_c, lower_a]

    Metric compatibility: Gamma^b_{ca} + Gamma^a_{cb} = 0
    i.e., Gamma[b,c,a] = -Gamma[a,c,b]

    Now A^a_{rs} = Gamma^s_{ra} - Gamma^r_{sa}
                 = Gamma[s,r,a] - Gamma[r,s,a]

    This is the antisymmetric part of Gamma[.,.,a] in the first two indices.

    For Killing e_a:
        Gamma[s,r,a] + Gamma[r,s,a] = 0  (metric Lie derivative zero -- WRONG)

    Wait, the Killing condition is:
        (L_{e_a} g)(e_r, e_s) = g(nabla_{e_r} e_a, e_s) + g(e_r, nabla_{e_s} e_a)
                                = Gamma^s_{ra} + Gamma^r_{sa}    [in ON frame]
                                = Gamma[s,r,a] + Gamma[r,s,a] = 0

    So for Killing: Gamma[s,r,a] = -Gamma[r,s,a]
    => A^a_{rs} = Gamma[s,r,a] - Gamma[r,s,a] = Gamma[s,r,a] - (-Gamma[s,r,a]) = 2*Gamma[s,r,a]

    This means the antisymmetric part does NOT vanish for Killing fields!

    Hmm. Let me re-read the Kosmann formula. From Paper 17 eq 4.1:

    L_X psi = nabla_X psi + (1/8) g^{ir} g^{js}
              [g(nabla_{v_i} X, v_j) - g(nabla_{v_j} X, v_i)] v_r v_s psi

    Actually checking the signs: the paper uses a specific convention.

    g(nabla_{v_i} X, v_j) = g(Gamma^k_{i,X} v_k, v_j) = Gamma^j_{i,X}

    Wait, X = e_a, so nabla_{v_i} e_a = Gamma^k_{ia} v_k, and
    g(nabla_{v_i} e_a, v_j) = Gamma^j_{ia} = Gamma[j,i,a]

    So the bracket is:
        g(nabla_{v_i} e_a, v_j) - g(nabla_{v_j} e_a, v_i) = Gamma[j,i,a] - Gamma[i,j,a]

    And the operator is:
        K_a = (1/8) sum_{i,j} [Gamma[j,i,a] - Gamma[i,j,a]] * g^{ir} g^{js} * gamma_r * gamma_s

    In ON frame, g^{ir} = delta^{ir}, g^{js} = delta^{js}, so:
        K_a = (1/8) sum_{r,s} [Gamma[s,r,a] - Gamma[r,s,a]] * gamma_r * gamma_s

    (renamed i->r, j->s for clarity)

    Now for CLOSING e_a: Gamma[s,r,a] + Gamma[r,s,a] = 0 => Gamma[s,r,a] = -Gamma[r,s,a]
    => bracket = Gamma[s,r,a] - Gamma[r,s,a] = -Gamma[r,s,a] - Gamma[r,s,a] = -2*Gamma[r,s,a]

    This is NOT zero! For Killing vectors, the Kosmann correction should reduce to the
    standard spin connection term (since Killing vectors generate isometries).

    Actually, the FULL Kosmann-Lichnerowicz derivative is:
        L_X psi = nabla_X psi + K_a psi

    For an isometry (Killing X), L_X should reduce to the standard action of the
    isometry on spinors: L_X psi = X(psi) + (1/4) omega_{ab}(X) gamma^a gamma^b psi
    where omega_{ab}(X) = Gamma_{abX} - Gamma_{baX} is antisymmetric.

    The nabla_X psi term is: nabla_X psi = X(psi) + (1/4) Gamma_{bc,X} gamma^b gamma^c psi
    (using the spin connection).

    Wait, I think I have the connection coefficient index order confused.
    Let me standardize:

    nabla_{e_a} e_b = Gamma^c_{ab} e_c   [convention: nabla_first second = Gamma^result_first_second]

    So: Gamma^c_{ab} = (nabla_{e_a} e_b)^c

    Metric compatibility: Gamma^c_{ab} + Gamma^b_{ac} = 0
    (swap upper with one lower, holding the nabla-direction fixed)

    Actually wait. Metric compatibility is:
    0 = nabla_{e_a} g(e_b, e_c) = g(nabla_{e_a} e_b, e_c) + g(e_b, nabla_{e_a} e_c)
    = Gamma^d_{ab} delta_{dc} + Gamma^d_{ac} delta_{db}
    = Gamma^c_{ab} + Gamma^b_{ac}

    So: Gamma[c, a, b] + Gamma[b, a, c] = 0 where indices are [upper, 1st_lower, 2nd_lower]

    This means Gamma is antisymmetric in (upper, 2nd_lower) when the 1st_lower is fixed:
    Gamma^c_{ab} = -Gamma^b_{ac}

    OK. Now for the Kosmann formula with X = e_a:
    g(nabla_{v_r} e_a, v_s) = Gamma^s_{ra}  [= Gamma[s, r, a]]

    Bracket: Gamma[s,r,a] - Gamma[r,s,a]

    Using metric compat: Gamma[s,r,a] = -Gamma[a,r,s]
    So: bracket = -Gamma[a,r,s] - Gamma[r,s,a]

    Hmm, this doesn't simplify nicely. Let me just compute it numerically.

    Parameters:
        Gamma: (8,8,8) connection coefficients, Gamma[c,a,b] = Gamma^c_{ab}
        gammas: list of 8 Clifford generators (16x16)
        a: direction index for the Kosmann derivative

    Returns:
        K_a: (16,16) complex matrix (Kosmann spinorial correction)
        A_antisym: (8,8) float, the antisymmetric tensor A^a_{rs}
    """
    dim_spin = gammas[0].shape[0]
    K = np.zeros((dim_spin, dim_spin), dtype=complex)
    A_antisym = np.zeros((8, 8), dtype=np.float64)

    for r in range(8):
        for s in range(8):
            # A^a_{rs} = Gamma^s_{ra} - Gamma^r_{sa}
            # In our array convention: Gamma[upper, 1st_lower, 2nd_lower]
            # Gamma^s_{ra} = Gamma[s, r, a]
            # Gamma^r_{sa} = Gamma[r, s, a]
            A_rs = Gamma[s, r, a] - Gamma[r, s, a]
            A_antisym[r, s] = A_rs
            if abs(A_rs) > 1e-15:
                K += A_rs * (gammas[r] @ gammas[s])

    K *= (1.0 / 8.0)

    return K, A_antisym


def kosmann_operator_symmetric(Gamma, gammas, a):
    """
    Compute the SYMMETRIC metric Lie derivative operator (what s22b used).

    S_a = (1/4) sum_{r,s} [Gamma^s_{ra} + Gamma^r_{sa}] gamma_r gamma_s
        = (1/4) (L_{e_a} g)^{rs} gamma_r gamma_s

    This is the WRONG operator for the Kosmann derivative but useful for
    comparison and validation.

    Parameters:
        Gamma: connection coefficients
        gammas: Clifford generators
        a: direction index

    Returns:
        S_a: (16,16) complex matrix
        Lg_sym: (8,8) float, the symmetric tensor (L_{e_a} g)_{rs}
    """
    dim_spin = gammas[0].shape[0]
    S = np.zeros((dim_spin, dim_spin), dtype=complex)
    Lg_sym = np.zeros((8, 8), dtype=np.float64)

    for r in range(8):
        for s in range(8):
            L_rs = Gamma[s, r, a] + Gamma[r, s, a]
            Lg_sym[r, s] = L_rs
            if abs(L_rs) > 1e-15:
                S += L_rs * (gammas[r] @ gammas[s])

    S *= 0.25

    return S, Lg_sym


def extract_singlet_eigensystem(tau, gens, f_abc, gammas):
    """
    Extract eigenvalues and eigenvectors of D_K in the (0,0) singlet sector.

    In the trivial irrep (0,0), dim=1, so D_pi = I_1 x Omega = Omega (16x16).
    We diagonalize H = 1j * Omega (Hermitian) using eigh.

    Parameters:
        tau: Jensen deformation parameter
        gens: SU(3) generators
        f_abc: structure constants
        gammas: Cliff(8) gamma matrices

    Returns:
        evals: (16,) real eigenvalues of (1j * D)
        evecs: (16,16) unitary, columns = eigenvectors
        Gamma: (8,8,8) connection coefficients
        E: (8,8) orthonormal frame
    """
    B_ab = compute_killing_form(f_abc)
    g_s = jensen_metric(B_ab, tau)
    E = orthonormal_frame(g_s)
    ft = frame_structure_constants(f_abc, E)
    Gamma = connection_coefficients(ft)
    Omega = spinor_connection_offset(Gamma, gammas)

    # Verify anti-Hermiticity
    ah_err = np.max(np.abs(Omega + Omega.conj().T))

    # Diagonalize
    H = 1j * Omega
    h_err = np.max(np.abs(H - H.conj().T))

    evals, evecs = eigh(H)

    return evals, evecs, Gamma, E, ah_err, h_err


def compute_kosmann_matrix_elements(evecs, K_a):
    """
    Project the Kosmann operator K_a into the eigenbasis of D_K.

    K_a^{nm} = <n|K_a|m> = (evecs^dag @ K_a @ evecs)_{nm}

    Parameters:
        evecs: (16,16) unitary, columns = eigenvectors
        K_a: (16,16) Kosmann operator in spinor basis

    Returns:
        K_eig: (16,16) complex, K_a in eigenbasis
    """
    return evecs.conj().T @ K_a @ evecs


